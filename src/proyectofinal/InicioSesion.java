    public void validar(){
        String usuario,contra;
        usuario=TF_usu.getText();
        contra=TF_con.getText();
        int entrar=0;
        String persona = "";
        try{
        connection con = new connection();
        ResultSet rs = con.conectar("SELECT clv_usuario,pass_usuario,tipo_usuario,idcarrera FROM usuarios");
        while (rs.next()) {
            String user = rs.getString("clv_usuario");
            String cont = rs.getString("pass_usuario");
            String tipo = rs.getString("tipo_usuario");
            String carrera = rs.getString("idcarrera");
            if(usuario.equals(user)&&contra.equals(cont)){
                entrar = 1;
                if(tipo.equals("ADMI")){
                    tipo = "Administrador";
                }else if (tipo.equals("DIRE")){
                    tipo = "Director";
                }else{
                    tipo = "Profesor";
                }
                SharedData.setTipoUsuario(tipo);
                SharedData.setClave(user);
                SharedData.setCarrera(carrera);
            }
        }
        if (entrar == 1){
            Menu abrir = new Menu();
            abrir.setVisible(true);
            abrir.setLocationRelativeTo(null);
            abrir.setResizable(false);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this,"Ingrese datos correctos","Error",JOptionPane.INFORMATION_MESSAGE);

        }
        con.desconectar();
        }catch(SQLException ex){
        }
    }
    
